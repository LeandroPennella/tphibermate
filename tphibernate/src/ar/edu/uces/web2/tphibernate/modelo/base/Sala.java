package ar.edu.uces.web2.tphibernate.modelo.base;

public class Sala {

	private int id;
	private String descripcion;
	
	public Sala() {
	
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getDescripcion() {
		return descripcion;
	}
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	
}
