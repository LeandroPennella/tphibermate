package ar.edu.uces.web2.tphibernate.modelo.base;

public class Contrasenia {

	private int id;
	private String valor;
	
	public Contrasenia(){}
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getValor() {
		return valor;
	}
	public void setValor(String valor) {
		this.valor = valor;
	}
	
	

}
